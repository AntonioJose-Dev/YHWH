/**
 * Éx 27:1 — "jamésh ammót órej vejamésh ammót rójav... veshálosh
 * ammót qomató". 5 × 5 × 3 codos. La ÚNICA pieza CUADRADA (rabúaʻ):
 * autenticación igual desde cualquier lado. Sin privilegio espacial.
 */
#define HASHMAL_AUTH_LARGO        5    /* v.1 — jamésh ammót órej */
#define HASHMAL_AUTH_ANCHO        5    /* v.1 — jamésh ammót rójav */
#define HASHMAL_AUTH_ALTO         3    /* v.1 — shalósh ammót qomató */
#define HASHMAL_AUTH_RABUAY       true /* v.1 — rabúaʻ: cuadrado */

/**
 * Éx 27:2 — "arbaʻ pinnotáv mimménnu tihyéna qarnotáv". 4 cuernos
 * en las 4 esquinas, nacen DEL altar. 1 Re 1:50 — los cuernos son
 * lugar de REFUGIO (Adonías se aferra a ellos).
 */
#define HASHMAL_AUTH_CUERNOS      4    /* v.2 — arbaʻ pinnotáv */

/**
 * Éx 27:2 — "vetsippíta otó NEJÓSHET". Bronce, NO oro. Zona
 * exterior, pública. Primera capa de confianza.
 */
#define HASHMAL_AUTH_NEJOSHET     true /* v.2 — bronce */

/**
 * Éx 27:3 — 5 utensilios (sirot, yaʻim, mizraqot, mizlegot, majtot).
 * "lejol kelív taʻasé nejóshet" — TODOS de bronce.
 */
#define HASHMAL_AUTH_UTENSILIOS   5    /* v.3 — 5 kelím */

/**
 * Éx 27:4 — "maʻasé réshet nejóshet veʻasíta ʻal harréshet arbaʻ
 * TABBEʻÓT nejóshet". Red con 4 anillos en las 4 esquinas de la red.
 */
#define HASHMAL_AUTH_ANILLOS_RESHET  4    /* v.4 — arbaʻ tabbeʻót */

/**
 * Éx 27:5 — "vehayetá harréshet ʻad JATSÍ hammizbéaj". La red
 * llega a la MITAD del altar. Interfaz entre exterior e interior.
 */
#define HASHMAL_AUTH_RESHET_MITAD    true /* v.5 — jatsí hammizbéaj */

/**
 * Éx 27:8 — "NEVÚV lujót taʻasé otó". HUECO. Stateless por diseño.
 * Cada autenticación es fresca; el altar no retiene sesión entre
 * pruebas. "kaashér herʼá otejá bahár" — patrón del monte.
 */
#define HASHMAL_AUTH_NEVUV        true /* v.8 — hueco = stateless */

/**
 * Lv 6:6 — "ÉSH TAMÍD tuqad ʻal hammizbéaj LO TIJBÉ". Fuego
 * perpetuo, nunca se apaga. Mismo espíritu que la Menorá (v.37
 * tamíd) y el Altar de Incienso (Éx 30:8 qetóret tamíd).
 */
#define HASHMAL_AUTH_TAMID        true /* Lv 6:6 — ésh tamíd */

/**
 * Éx 29:37 — "shivʻát yamím tekhappér ʻal hammizbéaj": 7 días de
 * expiación/consagración. Período de boot / calibración antes del
 * servicio regular.
 */
#define HASHMAL_AUTH_CONSAGRACION  7    /* Éx 29:37 — shivʻát yamím */

/**
 * Éx 29:38-39 — "shtéi kevasím... et hakkéves haejád taʻasé
 * babbóqer veʼet hakkéves hashení taʻasé béin haʻarbáyim". 2
 * corderos tamíd por día (mañana + tarde). 2 ciclos de verificación.
 */
#define HASHMAL_AUTH_VERIFICACIONES  2    /* Éx 29:38 — 2/día */

/**
 * Éx 40:6 — "venatattá et mizbáj haʻolá lifnéi pétaj mishkán óhel
 * moʻéd". El altar se coloca en el paso 5 del levantamiento.
 */
#define HASHMAL_AUTH_ORDEN_BOOT   5    /* Éx 40:6 */

/**
 * Tamaños de prueba/desafío criptográfico. SHA-256 = 32 B. El
 * challenge puede incluir nonce + metadata hasta 64 B.
 */
#define HASHMAL_AUTH_PROOF_BYTES      32   /* SHA-256 = 32 B */
#define HASHMAL_AUTH_CHALLENGE_MAX    64   /* desafío máximo */

/**
 * P9 (Verificado #11) — Lv 6:3-4 — déshen (H1880) buffer.
 * Ring buffer de residuos producidos por cada verificación. El
 * sacerdote saca déshen "el maqóm tahór mikhúts lammajané" — lugar
 * limpio FUERA del campamento. En firmware: garbage collection
 * extraída por un módulo externo (típicamente hashmal_power o
 * healthcheck), no acumulable indefinidamente dentro del altar.
 * 16 entradas: tamaño moderado, suficiente entre extracciones
 * periódicas; si se llena, las más nuevas desplazan a las más
 * viejas (Lv 6:4 — el déshen se SACA, no se queda).
 */
#define HASHMAL_AUTH_DESHEN_BUFFER    16   /* Lv 6:3-4 — residuos */

/* ══════════════════════════════════════════════════════════════════
 * ESTRUCTURAS
 * ══════════════════════════════════════════════════════════════════ */

/**
 * P9 (Verificado #11) — Lv 6:3-4 — UNA entrada de déshen.
 * Cada verificación produce UNA ceniza: timestamp + tipo del evento
 * (challenge / validado / rechazado). El extractor (garbage
 * collector externo) lee y vacía periódicamente.
 */
typedef struct {
    uint64_t              timestamp_ms;
    hashmal_auth_evento_t tipo;
} hashmal_auth_deshen_t;

/**
 * Éx 27:2 — un CUERNO (qeren) del altar del sacrificio.
 *
 * 4 cuernos en las 4 esquinas (pinnotáv). Son lugar de REFUGIO
 * (1 Re 1:50 — Adonías se aferra a los cuernos buscando asilo).
 *
 * En firmware: endpoints de autenticación — cada cuerno puede
 * aceptar una solicitud de challenge/response.
 */
typedef struct {
    uint8_t  id;              /* 0..3 */
    bool     activo;          /* ¿cuerno aceptando solicitudes? */
    uint64_t solicitudes;     /* contador acumulado */
} hashmal_qeren_auth_t;

/**
 * EL ALTAR DEL SACRIFICIO — singleton. Bronce, NEVÚV (hueco),
 * fuego tamíd.
 */
typedef struct {
    /* Estado operativo */
    hashmal_auth_estado_t estado;

    /* v.1-8 — identidad estructural */
    bool     tamid;           /* Lv 6:6 — ésh tamíd lo tijbé */
    bool     nejoshet;        /* v.2 — material: bronce */
    bool     rabuay;          /* v.1 — cuadrado */
    bool     nevuv;           /* v.8 — hueco (stateless) */
    bool     reshet_activa;   /* v.4-5 — réshet operativa a media altura */

    /* v.2 — 4 cuernos en las 4 esquinas */
    hashmal_qeren_auth_t cuernos[HASHMAL_AUTH_CUERNOS];
    uint8_t  cuernos_activos;

    /* Contadores de servicio */
    uint64_t pruebas_validadas;      /* Éx 29:38 — sacrificios aceptados */
    uint64_t pruebas_rechazadas;     /* Lv 22:20 — "lo yihyé leratsón" */
    uint64_t challenges_emitidos;    /* desafíos generados */
    uint64_t sesiones_emitidas;      /* tokens emitidos tras auth OK */
    uint64_t registros_ledger;       /* Ap 6:9 — almas bajo el altar */

    /* Timestamps */
    uint64_t ultimo_ciclo_ms;        /* Éx 29:38 — mañana o tarde */
    uint64_t ultimo_challenge_ms;
    uint64_t ultimo_registro_ms;     /* Ap 6:9 — último psyché registrado */

    /* Éx 27:8 — "kaashér herʼá otejá bahár": conforme al patrón */
    bool     conforme_al_tavnit;

    /* P9 (Verificado #11) — Lv 6:3-4 — déshen ring buffer.
     * Cada verificación añade UNA entrada; el extractor (garbage
     * collector externo, hashmal_power / healthcheck) saca al
     * "lugar limpio fuera del campamento" periódicamente. */
    hashmal_auth_deshen_t deshen[HASHMAL_AUTH_DESHEN_BUFFER];
    uint8_t  deshen_head;          /* próximo slot de escritura */
    uint8_t  deshen_count;         /* slots ocupados (≤ buffer) */
    uint64_t deshen_descartados;   /* contador overflow (Lv 6:4 sacar pendiente) */

    /* P9 — Lv 6:5 babóqer babóqer: alimentación periódica externa.
     * Contador de "etsim" añadidas (cada llamada de fuego_alimentar). */
    uint64_t fuego_alimentaciones;
} hashmal_auth_t;

