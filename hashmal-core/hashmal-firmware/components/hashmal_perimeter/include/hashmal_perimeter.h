/**
 * Éx 27:10-16 — una COLUMNA del atrio. Cada una con su lado
 * asignado, flag de activación y timestamp de registro.
 */
typedef struct {
    uint8_t                    id;              /* 0..59 */
    hashmal_perimeter_lado_t   lado;            /* sur/norte/oeste/este/puerta */
    bool                       activa;          /* en pie */
    uint64_t                   registro_ms;     /* cuándo se levantó */
} hashmal_perimeter_columna_t;

/**
 * SOLICITUD de entrada — lo que alguien presenta al llegar a la
 * puerta. Identidad, intención y timestamp.
 */
typedef struct {
    uint8_t  nodo_id;              /* quién solicita */
    uint8_t  intencion_flags;      /* OR de HASHMAL_PERIM_INTENT_* */
    uint64_t timestamp_ms;
} hashmal_perimeter_solicitud_t;

/**
 * RESULTADO de la verificación en la puerta. Incluye admisión,
 * cuál de las 4 columnas de la puerta validó, e intención
 * verificada.
 */
typedef struct {
    bool     admitido;
    uint8_t  columna_validadora;   /* 0..3 — una de las 4 columnas de la puerta */
    uint8_t  intencion_verificada; /* flags confirmados */
    uint64_t timestamp_ms;
} hashmal_perimeter_resultado_t;

