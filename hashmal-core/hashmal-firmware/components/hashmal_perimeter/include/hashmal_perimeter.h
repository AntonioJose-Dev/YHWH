typedef struct {
    hashmal_shaar_estado_t estado;
    uint8_t                sesiones_activas;       /* en ACTIVO */
    uint8_t                sesiones_pendientes;    /* CONECTADO/VERIFICADO */
    uint32_t               conexiones_totales;     /* historico */
    bool                   stealth_activo;          /* P6: leʻummat */
    uint8_t                bases_configuradas;      /* P2: 0..4 */
} hashmal_shaar_info_t;

