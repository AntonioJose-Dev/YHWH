/**
 * Estados del atrio.
 *
 *   DESMONTADO  — no levantado (Nm 4 — empacado para migrar)
 *   LEVANTADO   — cortinas arriba, puerta cerrada (Éx 40:33 vayyáqem)
 *   ACTIVO      — operativo, puerta lista (tras activar)
 */
typedef enum {
    HASHMAL_PERIM_DESMONTADO = 0,   /* Nm 4 — empacado */
    HASHMAL_PERIM_LEVANTADO  = 1,   /* Éx 40:33 — vayyáqem */
    HASHMAL_PERIM_ACTIVO     = 2,   /* operativo */
} hashmal_perimeter_estado_t;

/**
 * Los 6 lados del atrio (Éx 27:9-16). El ESTE se divide en 3 partes:
 * dos "hombros" (katéf) de 15 codos cada uno + la puerta central
 * de 20 codos. Total este = 50 codos (15+20+15).
 */
typedef enum {
    HASHMAL_PERIM_LADO_SUR        = 0,  /* Éx 27:9  — négev / teimán */
    HASHMAL_PERIM_LADO_NORTE      = 1,  /* Éx 27:11 — tsafón */
    HASHMAL_PERIM_LADO_OESTE      = 2,  /* Éx 27:12 — yam */
    HASHMAL_PERIM_LADO_ESTE_IZQ   = 3,  /* Éx 27:14 — katéf 1 */
    HASHMAL_PERIM_LADO_ESTE_DER   = 4,  /* Éx 27:15 — katéf 2 */
    HASHMAL_PERIM_LADO_PUERTA     = 5,  /* Éx 27:16 — shaʻar */
} hashmal_perimeter_lado_t;

/**
 * Estado de la puerta única.
 */
typedef enum {
    HASHMAL_PERIM_PUERTA_CERRADA     = 0,
    HASHMAL_PERIM_PUERTA_ABIERTA     = 1,  /* Sal 118:19 pitjú li */
    HASHMAL_PERIM_PUERTA_VERIFICANDO = 2,  /* verificando intención */
} hashmal_perimeter_puerta_t;

/**
 * Flags de intención para entrar (Sal 100:4 + Sal 118:19). Bits
 * combinables: una solicitud debe tener las tres para considerarse
 * COMPLETA.
 */
typedef enum {
    HASHMAL_PERIM_INTENT_TODA     = 0x01u,  /* Sal 100:4 — acción de gracias */
    HASHMAL_PERIM_INTENT_TEHILLA  = 0x02u,  /* Sal 100:4 — alabanza */
    HASHMAL_PERIM_INTENT_TSEDEQ   = 0x04u,  /* Sal 118:19 — justicia */
    HASHMAL_PERIM_INTENT_COMPLETA = 0x07u,  /* las 3 juntas = entrada plena */
} hashmal_perimeter_intencion_t;

/**
 * Códigos de error con raíz bíblica.
 */
typedef enum {
    HASHMAL_PERIM_OK             =  0,
    HASHMAL_PERIM_ERR_NULL       = -1,   /* puntero NULL */
    HASHMAL_PERIM_ERR_INIT       = -2,   /* no levantado */
    HASHMAL_PERIM_ERR_LLENO      = -3,   /* 60 columnas ya registradas */
    HASHMAL_PERIM_ERR_LADO       = -4,   /* lado inválido */
    HASHMAL_PERIM_ERR_DUPLICADO  = -5,   /* columna ya registrada */
    HASHMAL_PERIM_ERR_PUERTA     = -6,   /* puerta cerrada / no disponible */
    HASHMAL_PERIM_ERR_INTENCION  = -7,   /* Sal 100:4 — intención incompleta */
    HASHMAL_PERIM_ERR_DESMONTADO = -8,   /* operación sobre atrio desmontado */
    HASHMAL_PERIM_ERR_MIQLAT     = -9,   /* Añadidura 05 - error miqlat generico */
    HASHMAL_PERIM_ERR_MIQLAT_LLENO = -10, /* Nm 35 - tabla refugiados a capacidad */
    HASHMAL_PERIM_ERR_MIQLAT_MALIC = -11, /* Nm 35:20 - refugiado ya juzgado malicioso */
    HASHMAL_PERIM_ERR_ORCHIM       = -12, /* Añadidura 21 - Gn 18 hospitalidad */
    HASHMAL_PERIM_ERR_SHAAR        = -13, /* Añadidura 22 - Ex 27:16 sha'ar */
} hashmal_perimeter_err_t;

