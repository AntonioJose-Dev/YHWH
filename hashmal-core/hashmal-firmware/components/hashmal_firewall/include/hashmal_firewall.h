int hashmal_pesaj_activar(const uint8_t *token, size_t tam);

/**
 * Ex 12:13 - verifica si un nodo "pasa" durante el modo Pesaj.
 *
 *   Pesaj INACTIVO  -> true (no hay emergencia, todos pasan)
 *   Pesaj ACTIVO    -> verifica:
 *      a) nodo_id < HASHMAL_FIREWALL_MAX_PARES (Ex 28:21 doce)
 *      b) nodo NO esta NIKRAT (Ex 30:33 cortado, INV-NIKRAT)
 *      c) las 3 marcas (mezuza_izq + mezuza_der + mashqof) son true
 *      Las tres condiciones se cumplen -> pasaj (Ex 12:13)
 *      Cualquiera falla -> negef (Ex 12:23 destructor entra)
 *
 * Actualiza contadores verificaciones_pasadas / _rechazadas.
 *
 * @return true si pasa; false en cualquier caso de rechazo.
 */
bool hashmal_pesaj_verificar(uint8_t nodo_id,
                              const hashmal_pesaj_marca_t *marca);

/**
 * Ex 12 - la sangre era para esa noche. Termina la emergencia.
 * Limpia el token, marca estado EXPIRADO momentaneamente y luego
 * INACTIVO. Idempotente sobre INACTIVO.
 */
int hashmal_pesaj_expirar(void);

/**
 * Lectura pura: estado actual del modo Pesaj.
 */
hashmal_pesaj_estado_t hashmal_pesaj_estado_actual(void);

/**
 * Lectura pura: snapshot completo (sin exponer el token raw).
 */
int hashmal_pesaj_info(hashmal_pesaj_info_t *out);

