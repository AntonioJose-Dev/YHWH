/**
 * Las 4 especias = 4 categorías de reglas equitativas (bad bevad,
 * Éx 30:34). Ninguna domina — todas pesan igual.
 *
 *   NATAF     flujo: lo que fluye naturalmente (rate/flow control)
 *             Éx 30:34 natáf + Job 36:27 "yigrá ʻitpéi máyim"
 *             (destila gotas de agua — flujo medido).
 *
 *   SHEJELET  profundidad: lo oculto bajo la superficie
 *             (deep inspection)
 *             Éx 30:34 shejélet + Sal 139:15 "riqmá betajtiyót
 *             haʼárets" (bordado en lo profundo).
 *
 *   JELBENAH  anomalía: huele mal solo pero mejora la mezcla
 *             (anomaly detection — el "outlier" necesario para
 *              probar la composición)
 *             Éx 30:34 jelbenáh + Ro 8:28 "pánta synergeí eis
 *             agathón" (todo coopera para bien).
 *
 *   LEVONA    pureza: incienso puro, blanco, sin mezcla
 *             (format/integrity)
 *             Éx 30:34 levoná zajá + Mal 1:11 "qetóret muggásh
 *             lishmí uminjá tehorá" (incienso presentado a mi
 *             nombre, ofrenda pura entre las naciones).
 */
typedef enum {
    HASHMAL_FIREWALL_REGLA_NATAF    = 0,  /* Éx 30:34 + Job 36:27 */
    HASHMAL_FIREWALL_REGLA_SHEJELET = 1,  /* Éx 30:34 + Sal 139:15 */
    HASHMAL_FIREWALL_REGLA_JELBENAH = 2,  /* Éx 30:34 + Ro 8:28 */
    HASHMAL_FIREWALL_REGLA_LEVONA   = 3,  /* Éx 30:34 + Mal 1:11 */
} hashmal_firewall_regla_t;

/**
 * Veredicto del filtrado de un mensaje.
 *
 *   TAHOR  limpio, pasa          — Éx 30:35 tahór + Lv 13:13 tahór
 *   TAME   impuro, rechazado     — Lv 13:14 tamé + Nm 19:20 tamé
 *   ZAR    extraño (fuego extr.) — Lv 10:1 ésh zará + Éx 30:33 zar
 */
typedef enum {
    HASHMAL_FIREWALL_TAHOR = 0,  /* Éx 30:35 + Lv 13:13 */
    HASHMAL_FIREWALL_TAME  = 1,  /* Lv 13:14 + Nm 19:20 */
    HASHMAL_FIREWALL_ZAR   = 2,  /* Lv 10:1 + Éx 30:33 */
} hashmal_firewall_veredicto_t;

/**
 * Nivel de penalización — escalación progresiva. Cada nivel acumula
 * las protecciones del anterior.
 *
 *   TAHOR     limpio, sin sanción
 *   AZHARAH   advertencia (1ª infracción; Ez 3:17 + Ez 33:3)
 *   MASAK     cortina/throttle (Is 22:8 masáj + Éx 26:36 cortina)
 *   HEREM     prohibición total (Lv 27:28 jérem + Jos 6:17 Jericó)
 *   NIKRAT    expulsión definitiva / karát
 *             (Éx 30:38 nikrat + Éx 30:33 nikrat)
 */
typedef enum {
    HASHMAL_FIREWALL_NIVEL_TAHOR   = 0,
    HASHMAL_FIREWALL_NIVEL_AZHARAH = 1,  /* Ez 3:17 + Ez 33:3 */
    HASHMAL_FIREWALL_NIVEL_MASAK   = 2,  /* Is 22:8 + Éx 26:36 */
    HASHMAL_FIREWALL_NIVEL_HEREM   = 3,  /* Lv 27:28 + Jos 6:17 */
    HASHMAL_FIREWALL_NIVEL_NIKRAT  = 4,  /* Éx 30:38 + Éx 30:33 */
} hashmal_firewall_nivel_t;

/**
 * Estado del circuit breaker — detiene el tráfico ante emergencia.
 *
 *   CERRADO   flujo normal (Lv 16:12-13 nube de incienso protege —
 *             tráfico en curso bajo cobertura).
 *   ABIERTO   todo detenido (Nm 17:12 vayyárots; Nm 17:13 vayyaʻamód
 *             bein hammetím uvein hajjayyím — Aarón de pie entre
 *             los vivos y los muertos, conteniendo la plaga).
 */
typedef enum {
    HASHMAL_FIREWALL_CB_CERRADO = 0,  /* Lv 16:12-13 + Éx 30:7-8 */
    HASHMAL_FIREWALL_CB_ABIERTO = 1,  /* Nm 17:12 + Nm 17:13 */
} hashmal_firewall_cb_t;

/* ══════════════════════════════════════════════════════════════════
 * ESTRUCTURAS
 * ══════════════════════════════════════════════════════════════════ */

/**
 * Estado de penalización por par (peer). Acumula infracciones y
 * nivel vigente. La escalación es monótona hacia arriba durante
 * el ciclo; `penalizacion_limpiar` reinicia.
 */
typedef struct {
    hashmal_firewall_nivel_t nivel;               /* nivel actual */
    uint16_t                 infracciones;        /* acumuladas */
    int64_t                  timestamp_ultima_ms; /* última infracción */
    int64_t                  timestamp_escalada_ms; /* última subida */
} hashmal_firewall_penalizacion_par_t;

/**
 * Estado del heartbeat dual — Éx 30:7-8. Cada ciclo completo
 * requiere una pareja mañana+tarde; `ciclos_completados` cuenta
 * los pares cerrados.
 */
typedef struct {
    bool     manana_activo;         /* mañana ejecutada este ciclo */
    bool     tarde_activo;          /* tarde ejecutada este ciclo */
    int64_t  timestamp_manana_ms;   /* última mañana */
    int64_t  timestamp_tarde_ms;    /* última tarde */
    uint32_t ciclos_completados;    /* pares mañana+tarde cerrados */
} hashmal_firewall_heartbeat_t;

/**
 * Contadores globales del filtrado. Permite telemetría sin exponer
 * el contenido de los mensajes.
 */
typedef struct {
    uint32_t mensajes_total;    /* procesados totales */
    uint32_t veredictos[HASHMAL_FIREWALL_VEREDICTOS_TOTAL];
                                /* [TAHOR, TAME, ZAR] */
    uint32_t por_regla[HASHMAL_FIREWALL_REGLAS_TOTAL];
                                /* rechazos por cada regla */
} hashmal_firewall_estadisticas_t;

/**
 * Singleton global del firewall.
 */
typedef struct {
    bool                                 activo;
    hashmal_firewall_cb_t                circuitbreaker;
    hashmal_firewall_penalizacion_par_t  pares[HASHMAL_FIREWALL_MAX_PARES];
    hashmal_firewall_heartbeat_t         heartbeat;
    hashmal_firewall_estadisticas_t      estadisticas;
    int64_t                              timestamp_ms;
    int64_t                              timestamp_inicio_ms;
} hashmal_firewall_t;

