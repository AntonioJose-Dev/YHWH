static inline bool par_id_valido(uint8_t par_id)
{
    return par_id < HASHMAL_FIREWALL_MAX_PARES;
}

static const char *nombre_nivel(hashmal_firewall_nivel_t n)
{
    switch (n) {
    case HASHMAL_FIREWALL_NIVEL_TAHOR:
        return "TAHOR (H2889 — puro, sin sanción)";
    case HASHMAL_FIREWALL_NIVEL_AZHARAH:
        return "AZHARAH (H2094 — advertencia, Ez 3:17 + Ez 33:3)";
    case HASHMAL_FIREWALL_NIVEL_MASAK:
        return "MASAK (H4539 — cortina/throttle, Dt 19:15 + Mt 18:16)";
    case HASHMAL_FIREWALL_NIVEL_HEREM:
        return "HEREM (H2764 — prohibición total, Gn 4:24 + Pr 24:16)";
    case HASHMAL_FIREWALL_NIVEL_NIKRAT:
        return "NIKRAT (H3772 — cortado, Éx 30:38 + Nm 14:22)";
    default:
        return "<nivel desconocido>";
    }
}

/**
 * Calcula el nivel correspondiente al numero de infracciones,
 * evaluando de mayor a menor (cada umbral absorbe los siguientes).
 * Umbrales fijos en el header: 1, 3, 7, 10.
 */
static hashmal_firewall_nivel_t nivel_para_infracciones(uint16_t n)
{
    if (n >= HASHMAL_FIREWALL_UMBRAL_NIKRAT)   return HASHMAL_FIREWALL_NIVEL_NIKRAT;
    if (n >= HASHMAL_FIREWALL_UMBRAL_HEREM)    return HASHMAL_FIREWALL_NIVEL_HEREM;
    if (n >= HASHMAL_FIREWALL_UMBRAL_MASAK)    return HASHMAL_FIREWALL_NIVEL_MASAK;
    if (n >= HASHMAL_FIREWALL_UMBRAL_AZHARAH)  return HASHMAL_FIREWALL_NIVEL_AZHARAH;
    return HASHMAL_FIREWALL_NIVEL_TAHOR;
}

/* ==================================================================
 * hashmal_firewall_penalizacion_registrar - Exodo 30:38 + Ezequiel 3:17
 *
 * "venikrat me'ammav" con escalacion progresiva. Cada infraccion
 * suma uno; cuando cruza un umbral, el nivel ASCIENDE (nunca baja
 * en este camino - la atalaya acumula lo que ve). Nikrat es
 * absorbente: una vez alcanzado, nuevas llamadas devuelven
 * ERR_NIKRAT sin alterar el contador.
 *
 * Validaciones:
 *   - Subsistema iniciado -> ERR_NO_INIT.
 *   - par_id en rango    -> ERR_PAR_INVALIDO.
 *   - Ya NIKRAT          -> ERR_NIKRAT (cortado, no hay mas que registrar).
 *
 * Side effects:
 *   - par.infracciones++ (con guard contra overflow)
 *   - par.nivel eleva si corresponde (monotono)
 *   - par.timestamp_ultima_ms = ahora
 *   - par.timestamp_escalada_ms = ahora  (solo si cambio el nivel)
 *
 * 2+ testigos:
 *   Ex 30:38  - venikrat me'ammav (el pico de la escalacion)
 *   Ex 30:33  - paralelo en el aceite (misma estructura)
 *   Ez 3:17   - atalaya advierte (nivel AZHARAH, primer umbral)
 *   Dt 19:15  - 2 o 3 testigos (MASAK)
 *   Gn 4:24   - 7 veces (HEREM)
 *   Nm 14:22  - 10 veces (NIKRAT)
 * ================================================================== */
int hashmal_firewall_penalizacion_registrar(uint8_t par_id)
{
    hashmal_firewall_t *f = hashmal_firewall_incienso_mut();
    if (f == NULL) {
        PEN_LOG_E("penalizacion_registrar: firewall no iniciado");
        return HASHMAL_FIREWALL_ERR_NO_INIT;
    }

    if (!par_id_valido(par_id)) {
        PEN_LOG_W("penalizacion_registrar: par_id %u fuera de rango",
                  (unsigned)par_id);
        return HASHMAL_FIREWALL_ERR_PAR_INVALIDO;
    }

    hashmal_firewall_penalizacion_par_t *p = &f->pares[par_id];

    /* Ex 30:38 - nikrat es definitivo. */
    if (p->nivel == HASHMAL_FIREWALL_NIVEL_NIKRAT) {
        PEN_LOG_W("penalizacion_registrar: par %u ya NIKRAT — "
                  "venikrat meʻammáv (Éx 30:38), definitivo",
                  (unsigned)par_id);
        return HASHMAL_FIREWALL_ERR_NIKRAT;
    }

    const int64_t ahora = hashmal_firewall_ahora_ms();
    const hashmal_firewall_nivel_t nivel_previo = p->nivel;

    /* Guard contra overflow - 65535 infracciones antes del cap. */
    if (p->infracciones < 0xFFFFu) {
        p->infracciones++;
    }
    p->timestamp_ultima_ms = ahora;

    /* Calcular nivel desde el contador; solo subir (monotono). */
    const hashmal_firewall_nivel_t nivel_calculado =
        nivel_para_infracciones(p->infracciones);

    hashmal_firewall_nivel_t nivel_nuevo = p->nivel;
    if ((unsigned)nivel_calculado > (unsigned)p->nivel) {
        nivel_nuevo                = nivel_calculado;
        p->nivel                   = nivel_calculado;
        p->timestamp_escalada_ms   = ahora;
    }

    f->timestamp_ms = ahora;

    const bool escalo = (nivel_nuevo != nivel_previo);

    if (!escalo) {
        PEN_LOG_I("penalizacion: par %u +1 infracción (total=%u), "
                  "nivel se mantiene en %s",
                  (unsigned)par_id,
                  (unsigned)p->infracciones,
                  nombre_nivel(nivel_nuevo));
        return HASHMAL_FIREWALL_OK;
    }

    /* Escalo - registrar con la severidad adecuada al nivel. */
    switch (nivel_nuevo) {
    case HASHMAL_FIREWALL_NIVEL_AZHARAH:
        PEN_LOG_I("par %u → AZHARAH (infracciones=%u, umbral=%u) — "
                  "atalaya advierte (Ez 3:17 + Ez 33:3)",
                  (unsigned)par_id,
                  (unsigned)p->infracciones,
                  (unsigned)HASHMAL_FIREWALL_UMBRAL_AZHARAH);
        break;
    case HASHMAL_FIREWALL_NIVEL_MASAK:
        PEN_LOG_W("par %u → MASAK (infracciones=%u, umbral=%u) — "
                  "2 o 3 testigos confirmaron (Dt 19:15 + Mt 18:16)",
                  (unsigned)par_id,
                  (unsigned)p->infracciones,
                  (unsigned)HASHMAL_FIREWALL_UMBRAL_MASAK);
        break;
    case HASHMAL_FIREWALL_NIVEL_HEREM:
        PEN_LOG_W("par %u → HEREM (infracciones=%u, umbral=%u) — "
                  "completitud del juicio (Gn 4:24 shivʻatáyim + "
                  "Pr 24:16)",
                  (unsigned)par_id,
                  (unsigned)p->infracciones,
                  (unsigned)HASHMAL_FIREWALL_UMBRAL_HEREM);
        break;
    case HASHMAL_FIREWALL_NIVEL_NIKRAT:
        PEN_LOG_E("par %u → NIKRAT (infracciones=%u, umbral=%u) — "
                  "veNIKRAT meʻammáv (Éx 30:38) — cortado, "
                  "definitivo (ʻéser peʻamím, Nm 14:22)",
                  (unsigned)par_id,
                  (unsigned)p->infracciones,
                  (unsigned)HASHMAL_FIREWALL_UMBRAL_NIKRAT);
        break;
    default:
        PEN_LOG_W("par %u escaló a nivel desconocido %d",
                  (unsigned)par_id, (int)nivel_nuevo);
        break;
    }

    return HASHMAL_FIREWALL_OK;
}

/* ==================================================================
 * hashmal_firewall_penalizacion_consultar - lectura pura
 *
 * Sin logs, sin side effects. Escribe el nivel actual del par.
 *
 *   out NULL             -> ERR_NULL
 *   sin init             -> ERR_NO_INIT
 *   par_id fuera de rango-> ERR_PAR_INVALIDO
 * ================================================================== */
int hashmal_firewall_penalizacion_consultar(uint8_t par_id,
                                             hashmal_firewall_nivel_t *out)
{
    if (out == NULL) return HASHMAL_FIREWALL_ERR_NULL;

    const hashmal_firewall_t *f = hashmal_firewall_get_incienso_interno();
    if (f == NULL) return HASHMAL_FIREWALL_ERR_NO_INIT;

    if (!par_id_valido(par_id)) {
        return HASHMAL_FIREWALL_ERR_PAR_INVALIDO;
    }

    *out = f->pares[par_id].nivel;
    return HASHMAL_FIREWALL_OK;
}

/* ==================================================================
 * hashmal_firewall_penalizacion_limpiar - Isaias 1:18 + Salmo 103:12
 *
 * "im yihyu jata'eijem kashshanim kassheleg yalbinu" (Is 1:18) +
 * "kerajoq mizraj mimma'arav hirjiq mimmennu et pesha'einu"
 * (Sal 103:12). Restauracion: nivel->TAHOR, infracciones->0,
 * timestamps->0 (excepto ultima operacion, que registra el perdon).
 *
 * La misericordia llega hasta HEREM - un par en jerem puede ser
 * limpiado y reincorporado. Pero NIKRAT es Ex 30:38 "venikrat
 * me'ammav" - definitivo. `limpiar` con nivel NIKRAT devuelve
 * ERR_NIKRAT sin modificar el estado.
 *
 * Validaciones:
 *   - Subsistema iniciado -> ERR_NO_INIT.
 *   - par_id en rango    -> ERR_PAR_INVALIDO.
 *   - Ya NIKRAT          -> ERR_NIKRAT (irreversible).
 *
 * 2+ testigos:
 *   Is 1:18     - kassheleg yalbinu (promesa explicita de limpieza)
 *   Sal 103:12  - kerajoq mizraj mimma'arav (distancia infinita,
 *                  libro distinto)
 *   Ex 30:38    - venikrat me'ammav (la linea que no se borra)
 * ================================================================== */
int hashmal_firewall_penalizacion_limpiar(uint8_t par_id)
{
    hashmal_firewall_t *f = hashmal_firewall_incienso_mut();
    if (f == NULL) {
        PEN_LOG_E("penalizacion_limpiar: firewall no iniciado");
        return HASHMAL_FIREWALL_ERR_NO_INIT;
    }

    if (!par_id_valido(par_id)) {
        PEN_LOG_W("penalizacion_limpiar: par_id %u fuera de rango",
                  (unsigned)par_id);
        return HASHMAL_FIREWALL_ERR_PAR_INVALIDO;
    }

    hashmal_firewall_penalizacion_par_t *p = &f->pares[par_id];

    if (p->nivel == HASHMAL_FIREWALL_NIVEL_NIKRAT) {
        PEN_LOG_W("penalizacion_limpiar: par %u en NIKRAT — "
                  "venikrat meʻammáv (Éx 30:38) es definitivo, "
                  "no se limpia",
                  (unsigned)par_id);
        return HASHMAL_FIREWALL_ERR_NIKRAT;
    }

    const hashmal_firewall_nivel_t nivel_previo = p->nivel;
    const uint16_t                  infr_previas = p->infracciones;
    const int64_t                  ahora = hashmal_firewall_ahora_ms();

    p->nivel                 = HASHMAL_FIREWALL_NIVEL_TAHOR;
    p->infracciones          = 0u;
    p->timestamp_ultima_ms   = ahora;
    p->timestamp_escalada_ms = 0;
    f->timestamp_ms          = ahora;

    PEN_LOG_I("par %u restaurado — %s (infr=%u) → %s — "
              "kasshéleg yalbínu (Is 1:18); kerajóq mizráj "
              "mimmaʻaráv (Sal 103:12)",
              (unsigned)par_id,
              nombre_nivel(nivel_previo),
              (unsigned)infr_previas,
              nombre_nivel(HASHMAL_FIREWALL_NIVEL_TAHOR));

    return HASHMAL_FIREWALL_OK;
}

/* ==================================================================
 * hashmal_firewall_penalizacion_estado - snapshot per-par
 *
 * Lectura pura, sin logs. Copia la struct completa del par
 * (nivel, infracciones, timestamps) al buffer del llamante.
 *
 *   out NULL             -> ERR_NULL
 *   sin init             -> ERR_NO_INIT
 *   par_id fuera de rango-> ERR_PAR_INVALIDO
 * ================================================================== */
int hashmal_firewall_penalizacion_estado(uint8_t par_id,
                         hashmal_firewall_penalizacion_par_t *out)
{
    if (out == NULL) return HASHMAL_FIREWALL_ERR_NULL;

    const hashmal_firewall_t *f = hashmal_firewall_get_incienso_interno();
    if (f == NULL) return HASHMAL_FIREWALL_ERR_NO_INIT;

    if (!par_id_valido(par_id)) {
        return HASHMAL_FIREWALL_ERR_PAR_INVALIDO;
    }

    memcpy(out, &f->pares[par_id], sizeof(*out));
    return HASHMAL_FIREWALL_OK;
}

