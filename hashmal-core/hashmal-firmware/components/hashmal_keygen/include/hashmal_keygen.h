/* ══════════════════════════════════════════════════════════════════
 * VERIFICADO #06 — Item 1 — Propagación de unción por contacto
 *
 * "veqiddashtá otám vehayú qódesh qodashím — KOL HANNOGÉAʻ BAHÉM
 *  YIQDÁSH." — Éxodo 30:29
 * "Los santificarás y serán santo de santos; todo el que los
 *  TOQUE será santo."
 *
 * Bypassa la secuencia primaria de Éx 30:26-28 (la cual sigue
 * vigente para `_ungir`). Mecanismo distinto: el primario es acto
 * del sacerdote (mashájta); este es CONSECUENCIA dinámica del
 * contacto entre objetos ya santificados y otros.
 *
 * LÍMITE DEL PADRE — Hageo 2:11-13 toráh sacerdotal:
 *   v.12: si la carne santa toca alimento INDIRECTAMENTE (a través
 *         del vestido), ¿se santifica? — los sacerdotes responden NO.
 *   v.13: si un impuro toca, ¿se contamina? — responden SÍ.
 *
 * La santidad NO se propaga por contacto INDIRECTO. Sólo el
 * primario (mashájta, by_contact=false) puede transferir. Una pieza
 * que recibió por contacto (by_contact=true) NO puede propagar más
 * — sería el "vestido" de Hageo 2:12 que lleva santidad pero no la
 * imparte.
 *
 * @param origen   pieza ungida (DEBE estar ungida y by_contact=false).
 * @param destino  pieza a recibir santidad por contacto.
 * @return OK si propagó (o destino ya ungido — idempotente);
 *         ERR_INIT si origen no está ungido;
 *         ERR_CONTACTO_INDIRECTO si origen es by_contact=true (Hageo 2:12);
 *         ERR_SECUENCIA si origen o destino fuera de los 8 del tavnít;
 *         ERR_INIT si la tabla de ungidos está llena (8/8).
 *
 * Testigos (Dt 19:15):
 *   1. Éx 30:29 — hanogéaʻ bahem yiqdásh (positivo: contacto santifica)
 *   2. Hageo 2:12 — negación de propagación indirecta (límite)
 * ══════════════════════════════════════════════════════════════════ */
int hashmal_keygen_propagar_por_contacto(uint8_t origen, uint8_t destino);

