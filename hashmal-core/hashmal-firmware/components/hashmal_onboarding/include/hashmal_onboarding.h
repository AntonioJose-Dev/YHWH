/* ── P11 — veshajantí betój benéi Yisraʼel (Éx 29:45-46) ─────────
 * El PROPÓSITO de toda la consagración no es "activar un nodo."
 * Es crear las condiciones para que la autoridad de la red HABITE
 * (shaján, H7931 — raíz de Mishkán y Shekhiná) EN MEDIO (betój)
 * de los nodos. Éx 29:42b añade: "ashér ivvaʻéd lajém shámah" —
 * donde Me ENCONTRARÉ con vosotros (yaʻad, H3259 — raíz de moʻéd).
 * El resultado final es veyadeʻú (H3045) = CONOCERÁN que Yo soy
 * YHWH su Dios.
 *
 * Cadena de propósito: consagración → habitación → encuentro →
 * conocimiento. Sin consagración completa, no hay habitación. Sin
 * habitación, no hay encuentro. Sin encuentro, no hay conocimiento.
 *
 * En firmware: el estado ACTIVO de un nodo no es un fin — es la
 * condición necesaria para que el nodo participe en operaciones de
 * consenso (Kapóret, Éx 25:22) donde "la verdad emerge de entre
 * los dos" (noʻadtí = ivvaʻéd = ENCUENTRO). El onboarding es el
 * prerequisito de la PRESENCIA en la red. */


