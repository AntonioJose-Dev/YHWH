/**
 * Estado del nodo en el pipeline de consagración.
 *
 *   CERO         — sin consagrar aún (Éx 29:1 antes del proceso;
 *                   Lv 8:1-2 "qáj et Aharón" inicio).
 *   EN_PROCESO   — dentro de los 7 días (Éx 29:35 shivʻát yamím;
 *                   Lv 8:33 no salir de la puerta).
 *   ACTIVO       — lanzado en día 8 (Lv 9:1 bayyóm hasheminí;
 *                   Nm 3:3 "hakkohaním hammeshujím").
 */
typedef enum {
    HASHMAL_ONBOARDING_CERO       = 0,  /* Éx 29:1 + Lv 8:1-2 */
    HASHMAL_ONBOARDING_EN_PROCESO = 1,  /* Éx 29:35 + Lv 8:33 */
    HASHMAL_ONBOARDING_ACTIVO     = 2,  /* Lv 9:1 + Nm 3:3 */
} hashmal_onboarding_estado_t;

/**
 * Las 7 fases SECUENCIALES de Éxodo 29 / Levítico 8. El orden es
 * parte de la especificación — no paralelizable, no conmutable.
 *
 *   RAJATS   lavar    Éx 29:4      + Lv 8:6
 *   HILBISH  vestir   Éx 29:5      + Lv 8:7-9
 *   MASHAJ   ungir    Éx 29:7      + Lv 8:12
 *   JATTAT   pecado   Éx 29:10-14  + Lv 8:14-17  (novillo)
 *   OLA      ascender Éx 29:15-18  + Lv 8:18-21  (1er carnero)
 *   MILLUIM  llenar   Éx 29:19-28  + Lv 8:22-29  (2do carnero)
 *   SHIVAT   7 días   Éx 29:35     + Lv 8:33-35
 */
typedef enum {
    HASHMAL_ONBOARDING_FASE_RAJATS  = 0,  /* Éx 29:4     + Lv 8:6 */
    HASHMAL_ONBOARDING_FASE_HILBISH = 1,  /* Éx 29:5     + Lv 8:7-9 */
    HASHMAL_ONBOARDING_FASE_MASHAJ  = 2,  /* Éx 29:7     + Lv 8:12 */
    HASHMAL_ONBOARDING_FASE_JATTAT  = 3,  /* Éx 29:10-14 + Lv 8:14-17 */
    HASHMAL_ONBOARDING_FASE_OLA     = 4,  /* Éx 29:15-18 + Lv 8:18-21 */
    HASHMAL_ONBOARDING_FASE_MILLUIM = 5,  /* Éx 29:19-28 + Lv 8:22-29 */
    HASHMAL_ONBOARDING_FASE_SHIVAT  = 6,  /* Éx 29:35    + Lv 8:33-35 */
} hashmal_onboarding_fase_t;

/**
 * Los 3 sellos de Éxodo 29:20 — sangre aplicada en 3 puntos del
 * cuerpo derecho del sacerdote. Patrón confirmado en Lv 14:14
 * (mismo orden y cuerpo en la purificación del leproso).
 *
 *   OZEN         oreja derecha  — RECIBIR (Éx 29:20 + Lv 14:14)
 *   BOHEN_YAD    pulgar derecho — PROCESAR (Éx 29:20 + Lv 14:14)
 *   BOHEN_REGEL  dedo pie der.  — TRANSMITIR (Éx 29:20 + Lv 14:14)
 */
typedef enum {
    HASHMAL_ONBOARDING_SELLO_OZEN        = 0,  /* Éx 29:20 + Lv 14:14 */
    HASHMAL_ONBOARDING_SELLO_BOHEN_YAD   = 1,  /* Éx 29:20 + Lv 14:14 */
    HASHMAL_ONBOARDING_SELLO_BOHEN_REGEL = 2,  /* Éx 29:20 + Lv 14:14 */
} hashmal_onboarding_sello_t;

/* ══════════════════════════════════════════════════════════════════
 * ESTRUCTURAS
 * ══════════════════════════════════════════════════════════════════ */

/**
 * Estado por nodo — "llenar la mano" individual.
 *
 *   sellos[3]            — oreja, pulgar, dedo pie (Éx 29:20).
 *   fases_completadas[7] — registro discreto de cada fase
 *                           (como los testigos del algoritmo ciego).
 *   config_firmada       — sangre+aceite aplicados en vestiduras
 *                           (Éx 29:21 + Lv 8:30).
 *   lockdown             — recursos dedicados; ninguno extraño
 *                           los come (Éx 29:33 vezár lo yokhál +
 *                           Éx 29:34 no llevar nada al día siguiente).
 *   nodo_predecesor_id   — P9 — Éx 29:29, quién vino ANTES (0xFF = ninguno).
 *                           Herencia de credenciales (bigdéi haqqódesh).
 */
typedef struct {
    uint8_t  estado;                              /* hashmal_onboarding_estado_t */
    uint8_t  fase_actual;                         /* hashmal_onboarding_fase_t */
    bool     sellos[HASHMAL_ONBOARDING_SELLOS_TOTAL];
    uint8_t  ciclo_actual;                        /* 0..7 (Éx 29:35) */
    bool     fases_completadas[HASHMAL_ONBOARDING_FASES_TOTAL];
    bool     config_firmada;                      /* Éx 29:21 + Lv 8:30 */
    bool     lockdown;                            /* Éx 29:33-34 */
    bool     cuarentena;                          /* fuera del campamento (Éx 29:14) */
    uint8_t  nodo_predecesor_id;                  /* P9 — Éx 29:29; 0xFF = ninguno */
    int64_t  timestamp_inicio_ms;                 /* arranque del pipeline */
    int64_t  timestamp_fase_ms;                   /* entrada a la fase actual */
} hashmal_onboarding_nodo_t;

/**
 * Singleton global del subsistema de consagración. Contiene la
 * tabla de 12 nodos y los contadores agregados.
 */
typedef struct {
    bool                       iniciado;
    hashmal_onboarding_nodo_t  nodos[HASHMAL_ONBOARDING_MAX_NODOS];
    uint8_t                    nodos_en_proceso;   /* contador EN_PROCESO */
    uint8_t                    nodos_activos;      /* contador ACTIVO */
    int64_t                    timestamp_ms;
} hashmal_onboarding_t;

