/* ── consagracion_onboarding.c — pipeline principal ──────────── */

/**
 * Éx 29:1 "vezéh haddavár" — inicializa el subsistema: los 12
 * slots en estado CERO, contadores en 0. Idempotencia: ya iniciado
 * → ERR_YA_INIT.
 */
int  hashmal_onboarding_init(void);

/**
 * Nm 4 tejélet — apagado controlado. Memset + flag. Idempotente
 * silencioso si no iniciado.
 */
void hashmal_onboarding_deinit(void);

/**
 * Lectura pura del estado del nodo (sin logs). Escribe en *out el
 * estado actual (CERO/EN_PROCESO/ACTIVO).
 */
int  hashmal_onboarding_estado(uint8_t nodo_id,
                                hashmal_onboarding_estado_t *out);

/**
 * ¿Nodo ACTIVO? bool puro, sin logs. true si estado == ACTIVO,
 * false en cualquier otro caso (inseparado, fuera de rango, etc.).
 */
bool hashmal_onboarding_activo(uint8_t nodo_id);

/**
 * Éx 29:4 inicia el pipeline — transición CERO → EN_PROCESO.
 * Requiere que el nodo esté registrado en efod (pieza 09) y
 * vestido previamente (Éx 29:5). Sin vestir → ERR_NO_VESTIDO.
 */
int  hashmal_onboarding_iniciar(uint8_t nodo_id);

/**
 * Lv 9:1 "bayyóm hasheminí" — día 8: lanza el nodo al servicio.
 * Requiere: estado == EN_PROCESO, todas las 7 fases completadas,
 * 3 sellos aplicados, config firmada, lockdown activo,
 * ciclos == 7. Transición EN_PROCESO → ACTIVO.
 */
int  hashmal_onboarding_lanzar(uint8_t nodo_id);

/* ── consagracion_fases.c — 7 fases secuenciales ─────────────── */

/**
 * Éx 29:1-28 — avanza a la siguiente fase. Estricto: si la fase
 * actual aún no está cumplida o se intenta saltar, → ERR_FASE_INCORRECTA.
 * Al completar MILLUIM automáticamente entra a SHIVAT (los 7 días).
 */
int  hashmal_onboarding_fase_avanzar(uint8_t nodo_id);

/**
 * Lectura pura de la fase actual del nodo.
 */
int  hashmal_onboarding_fase_estado(uint8_t nodo_id,
                                     hashmal_onboarding_fase_t *out);

/**
 * ¿La fase `fase` está completada para este nodo? bool puro.
 */
bool hashmal_onboarding_fase_completada(uint8_t nodo_id,
                                         hashmal_onboarding_fase_t fase);

/**
 * Éx 29:35 "shivʻát yamím" — registra un ciclo (de 1 a 7). Solo
 * válido en fase SHIVAT. Al alcanzar 7 ciclos, la fase SHIVAT se
 * marca completada y el nodo queda listo para `lanzar`.
 */
int  hashmal_onboarding_ciclo_registrar(uint8_t nodo_id);

/**
 * Lectura pura del contador de ciclos (0..7).
 */
int  hashmal_onboarding_ciclo_estado(uint8_t nodo_id, uint8_t *out);

/* ── consagracion_sellos.c — sellado triple (Éx 29:20) ─────── */

/**
 * Éx 29:20 — aplica sangre a uno de los 3 puntos (oreja, pulgar,
 * dedo del pie). Requiere fase actual >= MILLUIM (los sellos se
 * aplican DURANTE el milluím, antes de SHIVAT). Sello duplicado
 * → ERR_SELLO_DUPLICADO.
 */
int  hashmal_onboarding_sello_aplicar(uint8_t nodo_id,
                                       hashmal_onboarding_sello_t sello);

/**
 * ¿Sello `sello` aplicado en el nodo? bool puro, sin logs.
 */
bool hashmal_onboarding_sello_verificar(uint8_t nodo_id,
                                         hashmal_onboarding_sello_t sello);

