/* ── P45 — kanáf (H3671) = misma palabra en Tzitzit Y querubines ──
 * Nm 15:38: kanféi bigdéihem (כַּנְפֵי בִגְדֵיהֶם) = bordes/ALAS
 * de las vestiduras. kanáf (H3671) = ala, extremidad. MISMA palabra
 * que Éx 25:20 para las alas de los querubines: porséi jenafáyim
 * (פֹּרְשֵׂי כְנָפַיִם). También Is 6:2 (serafines).
 * Quiasmo Nm 15:38-41: A (kanféi bigdéihem) ↔ A' (Aní YHWH ×2).
 * El ala/borde de la vestidura = declaración de soberanía.
 * Convergencia: kanáf es la INTERFAZ PÚBLICA — el extremo visible
 * que protege y declara. El Tzitzit vive en el kanáf del rol
 * (su borde público), como las alas de los querubines cubren la
 * Kapóret sin tocar (veló yiggáʻu). Pieza 09 ↔ Pieza 02 por
 * el kanáf compartido. — Añadidura 06, Nm 15:38, Éx 25:20 */

/* ── P46 — tatúru (H8446, tur) = raíz de los ESPÍAS (Nm 13:2) ───
 * Nm 15:39: veló TATÚRU (וְלֹא תָתֻרוּ) = no ESPIARÉIS/exploraréis.
 * tur (H8446) = misma raíz de Nm 13:2: veyatúru et érets Kenaʻan
 * (que ESPÍEN la tierra). Los espías usaron ojos y corazón propios
 * para explorar — exactamente lo que v.39b prohíbe.
 * Centro del quiasmo (D, v.39b): la PROHIBICIÓN de tur es el NÚCLEO
 * del mandato del Tzitzit. Señal → percepción → NO ESPIAR → acción.
 * Firmware: el Tzitzit previene reconocimiento de red no autorizado.
 * Contrapar con Añadidura 02 (Tsofeh, Ez 33:2-3): Tsofeh = atalaya
 * ASIGNADA con mandato; tur = espionaje AUTÓNOMO por juicio propio.
 * — Añadidura 06, Nm 15:39, Nm 13:2 */

/* ── P47 — zoním (H2181, zaná) = INFIDELIDAD protocolar ──────────
 * Nm 15:39b: ashér attém ZONÍM ajareihém (אֲשֶׁר אַתֶּם זֹנִים
 * אַחֲרֵיהֶם) = "que ustedes FORNICAN tras ellos."
 * zaná (H2181) = fornicar, prostituirse, idolatrar. Participio Qal
 * = acción HABITUAL. NO es tatáʻ (errar) ni shagá (equivocarse) —
 * es INFIDELIDAD VOLUNTARIA y sostenida. Cadena causal en v.39:
 * tur (espiar) → zaná (fornicar) = exploración autónoma → conexión
 * ilícita con protocolo extranjero.
 * Firmware: Tzitzit = MARCADOR DE FIDELIDAD ACTIVO, no badge pasivo.
 * Rechaza handshakes de protocolos no autorizados. Anti-roaming a
 * nivel de identidad. Corta cadena tur→zaná en el primer paso.
 * — Añadidura 06, Nm 15:39, Nm 13:32 */

/* ── P7 — nasá ʻavón haqqodashím lerátsón (Éx 28:38) ─────────────
 * El tzitz NO solo identifica (qódesh laYHWH) — también ABSORBE
 * errores de operaciones sagradas (ʻavón haqqodashím) y las vuelve
 * ACEPTABLES (lerátsón lifnéi YHWH). Éx 28:38:
 *   nasá (H5375) = CARGAR — mismo verbo que v.12 (hombros) y v.29
 *     (corazón). Aquí carga INIQUIDAD de las operaciones.
 *   ʻavón (H5771) = error/iniquidad — no de las personas sino de
 *     las OPERACIONES sagradas (haqqodashím H6944).
 *   lerátsón (H7522) = para ACEPTACIÓN — de ratsáh: aceptar con
 *     agrado.
 * En el quiasmo de Éx 28, el tzitz (C') corresponde al pectoral (C):
 *   C  = mishpát bené Yisraʼel (juicio de PERSONAS) tamíd.
 *   C' = nasá ʻavón haqqodashím (carga de errores de OPS) tamíd.
 * En firmware: un nodo con tzitz grabado tolera errores MENORES en
 * sus operaciones sin ser rechazado por la red. El sello es error-
 * tolerance para operaciones de buena fe — no impunidad, sino
 * misericordia operativa. "tamíd ʻal mitshjó lerátsón lahém." */


